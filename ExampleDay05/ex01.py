# 산술연산자
print("{} + {} = {}".format(10,20,(10+20)))
print("{} - {} = {}".format(200,100,(200-100)))
print("{} * {} = {}".format(10,20,(10*20)))
print("{} ** {} = {}".format(2,3,(2**4)))
print("{} / {} = {}".format(4,2,(4/2)))
print("{} // {} = {}".format(3,2,(3//2))) # 몫
print("{} % {} = {}".format(3,2,(3%2))) # 나머지

val1 = int(input("숫자입력 >> "))
